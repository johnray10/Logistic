    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
       
        if(projectname.getText().isEmpty()
                ||jComboBox_priority.getSelectedItem()=="-Choose Here-" ||jComboBox_priority.getSelectedItem().equals("-Choose Here-")
                ||((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText().isEmpty()
                ||((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText().isEmpty()
                ||projectDescrip.getText().isEmpty()
                ||jComboBox2.getSelectedItem()=="-Choose Here-" ||jComboBox2.getSelectedItem().equals("-Choose Here-")){
                        JOptionPane.showMessageDialog(null,"This field cannot be empty!","ERROR!",JOptionPane.ERROR_MESSAGE);
        }else{
        
        
        int x = JOptionPane.showConfirmDialog(null, "Are You Sure You Want To Add Record?","Add Record",JOptionPane.YES_NO_OPTION);
        if(x==0){  
        try {
            String sql = "INSERT INTO Log1_project "
            + "(projectid, projectname, priority, startdate, enddate, projectdescription, status)"
            + "VALUES (?,?,?,?,?,?,?)";

            pst=conn.prepareStatement(sql);
            pst.setString(1,projID.getText());
            pst.setString(2,projectname.getText());
            pst.setString(3,jComboBox_priority.getSelectedItem().toString());
            pst.setString(4,((JTextField)jDateChooser2.getDateEditor().getUiComponent()).getText());
            pst.setString(5,((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText());
            pst.setString(6,projectDescrip.getText());
            pst.setString(7,jComboBox2.getSelectedItem().toString());
            pst.execute();  
            allproject() ;
           
            
            JOptionPane.showMessageDialog(null, "Add Successfull! ");   
             this.dispose();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }   
        }
        }
    }//GEN-LAST:event_AddActionPerformed

