    private void pm1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pm1KeyReleased
       try{
            String sql = "select * from log1_Requestmaterials where RequestID=?";
            pst=conn.prepareCall(sql);
            pst.setString(1,pm1.getText());
            rs = pst.executeQuery();

            if(rs.next()) {
                pm2.setText(rs.getString("Category"));
                pm3.setText(rs.getString("SubCategory"));
                pm4.setText(rs.getString("Quantity"));
                pm5.setText(rs.getString("DateNeeded"));
                pm6.setText(rs.getString("Purpose"));
                pm7.setText(rs.getString("Status"));
              
            }else{
           pm2.setText("");
           pm3.setText("");
           pm4.setText("");
           pm5.setText("");
           pm6.setText("");
           pm7.setText("");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_pm1KeyReleased

