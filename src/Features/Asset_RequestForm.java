    public void autoID() {
     try {
         String sql = "SELECT ReceivedID FROM Log1_Assetrequest ORDER BY ReceivedID DESC";
         pst=conn.prepareStatement(sql);
         rs=pst.executeQuery();
         if(rs.next())
         {
             String rnno = rs.getString("ReceivedID");
             int co = rnno.length();
             String txt = rnno.substring(0, 2);
             String num = rnno.substring(2, co);
             int n = Integer.parseInt(num);
             n++;
             String snum=Integer.toString(n);
             String ftxt=txt+snum;
             txtReceiveID.setText(ftxt);
         }else{
             txtReceiveID.setText("100");
         }
     } catch (Exception e) {
         JOptionPane.showMessageDialog(null, e);
     }
 }
