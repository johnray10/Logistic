    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed

        if(jComboBox2.getSelectedItem()=="Choose here"||jComboBox2.getSelectedItem().equals("Choose here")
          ||txtSubcat.getText().isEmpty()
          ||txtRequest.getText().isEmpty()
          ||txtposition.getText().isEmpty()
          ||txtdept.getText().isEmpty()
          ||((JTextField)Requestdate.getDateEditor().getUiComponent()).getText().isEmpty()
          ||txtpurpose.getText().isEmpty()){
                    JOptionPane.showMessageDialog(null,"This field cannot be empty!","ERROR!",JOptionPane.ERROR_MESSAGE);
        }else{
        
        int x = JOptionPane.showConfirmDialog(null, "Are You Sure You Want To Add Record?","Add Record",JOptionPane.YES_NO_OPTION);
        if(x==0){      
        try{
        String sql = "INSERT INTO Log1_AssetRequest "
            + "(ReceivedID, AssetCategory, SubCategory, RequestBy, Position, Department, RequestDate, Purpose)"
            + "VALUES(?,?,?,?,?,?,?,?)";

            pst=conn.prepareStatement(sql);  
            pst.setString(1,txtReceiveID.getText());
            String value1=jComboBox2.getSelectedItem().toString();
            pst.setString(2, value1);  
            pst.setString(3,txtSubcat.getText());
            pst.setString(4,txtRequest.getText());
            pst.setString(5,txtposition.getText());
            pst.setString(6,txtdept.getText());
            pst.setString(7, ((JTextField)Requestdate.getDateEditor().getUiComponent()).getText());
            pst.setString(8,txtpurpose.getText());          
            pst.executeUpdate();
            AssetRequest();
          getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Add Successfull! ");        
            
            
            
               Toaster toasterManager = new Toaster();
               toasterManager.showToaster("New Item Request Received From Warehouse");
               
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
        }
    }//GEN-LAST:event_SubmitActionPerformed
