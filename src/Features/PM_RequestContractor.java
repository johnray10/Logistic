    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       
              try{
            int row = jTable1.getSelectedRow();
            String Table_click=(jTable1.getModel().getValueAt(row, 0).toString());
            String sql = "select * from log2_vendor_pm where request_id='"+Table_click+"' ";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()) {
              RequestID.setText(rs.getString("request_id"));
              jComboBox1.setSelectedItem(rs.getString("Contractor_Type"));
                ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).setText(rs.getString("Date_Needed"));
                jTextArea1.setText(rs.getString("Purpose"));
                jComboBox2.setSelectedItem(rs.getString("status"));
          }           
        }catch(Exception e) {          
            JOptionPane.showMessageDialog(null, e);
        }
       
     
        
    }//GEN-LAST:event_jTable1MouseClicked
