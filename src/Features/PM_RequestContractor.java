    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        if(RequestID.getText().isEmpty()
                ||jComboBox1.getSelectedItem()=="-SELECT-" || jComboBox1.getSelectedItem().equals("-SELECT-")
                ||((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText().isEmpty()
                ||jTextArea1.getText().isEmpty()
                ||jComboBox2.getSelectedItem()=="-SELECT" ||jComboBox2.getSelectedItem().equals("-SELECT-")
                ||contractor.getText().isEmpty()){
                 JOptionPane.showMessageDialog(null,"This field cannot be empty!","ERROR!",JOptionPane.ERROR_MESSAGE);
        }else{
        
     int x = JOptionPane.showConfirmDialog(null, "Are You Sure You Want To Add Record?","Add Record",JOptionPane.YES_NO_OPTION);
        if(x==0){  
        try {
            String sql = "INSERT INTO log2_vendor_pm"
            + "(Request_ID, Contractor_Type, Date_Needed, Purpose, Status, Contractor)"
            + "VALUES (?,?,?,?,?,?)";

            pst=conn.prepareStatement(sql);
            pst.setString(1,RequestID.getText());
            pst.setString(2,jComboBox1.getSelectedItem().toString());
            pst.setString(3, ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText());
            
            pst.setString(4,jTextArea1.getText());
            pst.setString(5,jComboBox2.getSelectedItem().toString());
            pst.setString(6,contractor.getText());
            
            pst.executeUpdate();  
            contractor();
         
            
            JOptionPane.showMessageDialog(null, "Add Successfull! ");   
            
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }   
        }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

