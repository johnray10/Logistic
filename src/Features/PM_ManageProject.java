    private void ProjectIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ProjectIDKeyReleased
        try{
          String sql = "SELECT * FROM Log1_project WHERE projectid=?";
          pst=conn.prepareCall(sql);
          pst.setString(1,ProjectID.getText());
          rs = pst.executeQuery();          
        if(rs.next()) { 
            ProjectName.setText(rs.getString("ProjectName"));
            jComboBox_priority.setSelectedItem(rs.getString("Priority"));
            ((JTextField)start.getDateEditor().getUiComponent()).setText(rs.getString("Startdate"));
            ((JTextField)End.getDateEditor().getUiComponent()).setText(rs.getString("EndDate"));
            projectDescrip.setText(rs.getString("ProjectDescription"));
            jComboBox2.setSelectedItem(rs.getString("Status"));           
          }else{
             ProjectName.setText("");
            jComboBox_priority.setSelectedItem("");
            ((JTextField)start.getDateEditor().getUiComponent()).setText("");
            ((JTextField)End.getDateEditor().getUiComponent()).setText("");
            projectDescrip.setText("");
            jComboBox2.setSelectedItem("");   
        }       
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null, e);
        }
    
    }//GEN-LAST:event_ProjectIDKeyReleased

