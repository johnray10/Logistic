    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
          try{
            int row = jTable1.getSelectedRow();
            String Table_click=(jTable1.getModel().getValueAt(row, 0).toString());
            
            String sql = "select * from Log1_project where projectid='"+Table_click+"' ";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()) {
                ProjectID.setText(rs.getString("ProjectID"));
                ProjectName.setText(rs.getString("ProjectName"));
                jComboBox_priority.setSelectedItem(rs.getString("Priority"));
                ((JTextField)start.getDateEditor().getUiComponent()).setText(rs.getString("StartDate"));
                ((JTextField)End.getDateEditor().getUiComponent()).setText(rs.getString("EndDate"));
                projectDescrip.setText(rs.getString("projectdescription"));
                jComboBox2.setSelectedItem(rs.getString("Status"));      
          }           
        }catch(Exception e) {          
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jTable1MouseClicked

